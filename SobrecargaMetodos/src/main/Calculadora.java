package main;

public class Calculadora {
    //Sobrecarga metodos

    // Suma Enteros
    public int suma(int valor1, int valor2){
        return valor1 + valor2;
    }

    // Suma Flotantes
    public float suma(float valor1, float valor2){
        return valor1 + valor2;
    }

    // Suma Doubles
    public double suma(double valor1, double valor2){
        return valor1 + valor2;
    }



}
